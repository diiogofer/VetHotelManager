package hva.core;

/**
 * Enum representing the types of employees.
 * There are two types of employees:
 * <ul>
 *   <li>VETERINARIAN - A veterinarian responsible for the health and medical care of the animals.</li>
 *   <li>KEEPER - A keeper responsible for the maintenance of the habitats.</li>
 * </ul> 
 */
public enum EmployeeType {
    /** Represents a veterinarian employee. */
    VETERINARIAN, 
    
    /** Represents a keeper employee. */    
    KEEPER;
}
