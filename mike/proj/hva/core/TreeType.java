package hva.core;

/**
 * Enum representing the types of trees that can exist.
 * 
 * <p>The available tree types are:</p>
 * <ul>
 *   <li>PERENE - evergreen tree.</li>
 *   <li>CADUCA - deciduous tree.</li>
 * </ul>
 */
public enum TreeType {

    /** Represents a evergreen tree. */
    PERENE, 
    
    /** Represents a deciduous tree. */
    CADUCA;
}
